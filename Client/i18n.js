let currentLocale = readSavedLocale();

export function getLocale() {
  return currentLocale;
}

export function setLocale(locale) {
  currentLocale = normalizeLocale(locale);
  localStorage.setItem(LOCALE_STORAGE_KEY, currentLocale);
  document.documentElement.lang = getLocaleMeta(currentLocale).htmlLang;
}

export function t(key, params = {}) {
  const message = dictionaries[currentLocale]?.[key] ?? dictionaries[DEFAULT_LOCALE]?.[key] ?? key;
  return message.replace(/\{(\w+)\}/g, (_, name) => (params[name] ?? params[name] === 0 ? String(params[name]) : ""));
}

export function translatePageStaticText(root = document) {
  document.documentElement.lang = getLocaleMeta(currentLocale).htmlLang;
  document.title = t("app.title");

  root.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });

  root.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel));
  });

  root.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder));
  });

  root.querySelectorAll("[data-i18n-title]").forEach((element) => {
    element.setAttribute("title", t(element.dataset.i18nTitle));
  });
}

export function setupLocaleSwitcher(selectElement, onChange = () => {}) {
  if (!selectElement) {
    return;
  }

  selectElement.value = currentLocale;
  selectElement.setAttribute("aria-label", t("language.label"));
  selectElement.addEventListener("change", () => {
    setLocale(selectElement.value);
    selectElement.setAttribute("aria-label", t("language.label"));
    onChange(currentLocale);
  });
}

function readSavedLocale() {
  return normalizeLocale(localStorage.getItem(LOCALE_STORAGE_KEY));
}

function normalizeLocale(locale) {
  return SUPPORTED_LOCALES.some((item) => item.code === locale) ? locale : DEFAULT_LOCALE;
}

function getLocaleMeta(locale) {
  return SUPPORTED_LOCALES.find((item) => item.code === locale) || SUPPORTED_LOCALES[0];
}
